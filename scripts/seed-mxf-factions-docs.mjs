async function ensureProduct() {
  return prisma.product.upsert({
    where: { slug: productSeed.slug },
    update: {
      documentationLink: productSeed.documentationLink,
      supportLink: productSeed.supportLink,
      visible: true,
    },
    create: productSeed,
  });
}

async function main() {
  const product = await ensureProduct();

  for (const article of articles) {
    await prisma.documentationArticle.upsert({
      where: { slug: article.slug },
      update: {
        title: article.title,
        category: article.category,
        excerpt: article.excerpt,
        bodyMarkdown: article.bodyMarkdown,
        version: product.version,
        productId: product.id,
        productVersion: product.version,
        visible: true,
        sortOrder: article.sortOrder,
      },
      create: {
        title: article.title,
        slug: article.slug,
        category: article.category,
        excerpt: article.excerpt,
        bodyMarkdown: article.bodyMarkdown,
        version: product.version,
        productId: product.id,
        productVersion: product.version,
        visible: true,
        sortOrder: article.sortOrder,
      },
    });
  }

  console.log(
    JSON.stringify(
      {
        ok: true,
        product: product.slug,
        docs: articles.length,
        slugs: articles.map((article) => article.slug),
      },
      null,
      2,
    ),
  );
}

main()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
